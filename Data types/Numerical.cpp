#include <iostream>
using namespace std;

//numerical data types include int,float,double

int main(){
    int num1 = 1000;  //int are whole numbers without decimals

    float num2 = 14.986; //float are numbers with decimals.

    double num3 = 19.968; //double are also numbers with decimals.

    float num4 = 35e4;

    double num5 = 12e-4;

    cout << num1 << endl;
    cout << num2 << endl;
    cout << num3 << endl;
    cout << num4 << endl;
    cout << num5 << endl;
    
    


    return 0;
}